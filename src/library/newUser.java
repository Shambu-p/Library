    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:

            UsersInterface user_db = (UsersInterface) Naming.lookup("rmi://"+Library.server_adress+"/Users");
            String name = this.user_name_field.getText(); 
            if(!name.isEmpty()){
                if(user_db.create(name, "default_user.jpg")){
                    JOptionPane.showMessageDialog(null, "User Created", "Message", JOptionPane.PLAIN_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "User Creation Failed", "Message", JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Empty User name");
            }
            
        } catch (NotBoundException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (MalformedURLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
        Library.creation_page.setVisible(false);
        
    }//GEN-LAST:event_jButton1ActionPerformed

