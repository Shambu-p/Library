    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try {
            // TODO add your handling code here:

            BooksInterface book_db = (BooksInterface) Naming.lookup("rmi://"+Library.server_adress+"/Books");
            String title = this.title_field.getText(); 
            String category = this.category_field.getText();
            
            if(!title.isEmpty() && !category.isEmpty()){
                if(book_db.create(title, category, this.shelf_nums.getSelectedIndex()+1, "default_book.png")){
                    JOptionPane.showMessageDialog(null, "Book Added", "Message", JOptionPane.PLAIN_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Book Creation Failed", "Message", JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Empty field encauntered please finish the form first!", "Error Message", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (NotBoundException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (MalformedURLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
        Library.creation_page.setVisible(false);
        
    }//GEN-LAST:event_jButton1ActionPerformed

